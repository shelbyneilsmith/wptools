<h3>Wireframes</h3>

<p>Wireframes deal with <em>structure</em>. The purpose of a wireframe is to demostrate how we think the website is best organized. Wireframes show how the user will navigate your website, how content will be ordered, what features are present, and what areas will be most prominent.</p>

<p>To show how your website will react to multiple screen sizes, we have included controls on the bottom of each wireframe page. These controls will resize the window to commonly found screen resolutions, causing the content of the website to reorganize itself based on the viewing area.</p>

<h3>Questions to ask youself when reviewing a wireframe:</h3>

<ul>
	<li>What is the first thing a user sees, and why? Is the most important thing to my business right now?</li>
	<li>Will users be able to quickly find all the sections of my website?</li>
	<li>What pages/features do you think will be particulary useful for your business, and why? Our goal is the build the best website for your business, and we want all the industry insight and experience we can gather.</li>
	<li>What pages/features do you think will be ineffective, and why?</li>
	<li>Are there any features I think my customers could benefit from that are not included. <span class="small-text">(Now is the time to add new features, it only becomes more difficult later.)</span></li>
</ul>

<h3>Style Tiles</h3>

<p>Style tiles are on the complete other end of the spectrum. It's right in the name, it's all about sttttttyyyylle. No but seriously, style tiles demostrate the <em>tone</em> and visual style that your final site will have. Think carefully about the you want people to perceive your business. Do you want to appear edgy, youthful, friendly, laid-back, aggresive, modern, institutional, or some other emotion? The colors, typography, and visual elements all help shape what people think about your website and your brand.</p>

<h3>Questions to ask yourself when reviewing a style prototype:</h3>

<ul>
	<li>What is the attitude of my business? What should be the attitude of my business?</li>
	<li>Do you like the visual style? Will your customers react positively to the visual style? Which is more important?</li>
<li>Is this consistant with the rest of my brand?</li>
</ul>